using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SimpleCalculator
{
    class Calculator
    {
        public double add(double aNumber, double bNumber)
        {
            return (aNumber + bNumber);
 
        }
        public double subtract(double aNumber, double bNumber)
        {
            return (aNumber - bNumber);

        }
        public double multiply(double aNumber, double bNumber)
        {
            return (aNumber * bNumber);

        }
        public double divide(double aNumber, double bNumber)
        {
            return (aNumber / bNumber);

        }

    }
}
